// Конфиг pm2 для прод-процесса этого сайта (где-заправиться.рф).
// Standalone-сборка Next.js, запуск/обновление: npm run deploy (см. scripts/deploy.sh).
//
// Этот сайт живёт на той же VPS, что и оригинальный форк (benzryadom.ru), как
// отдельный процесс — см. docs/SHARED-INFRA.md. Порт и имя приложения ниже
// намеренно отличаются от оригинала (там "benzin-map" на 3000), чтобы
// `pm2 start ecosystem.config.js` здесь не задел его процесс. Тайл-сервер
// ("benzin-tiles", 127.0.0.1:8081) — общий для обоих сайтов синглтон,
// управляется отдельно от любого из репозиториев и намеренно не описан тут.
module.exports = {
  apps: [
    {
      name: "gde-zapravitsya",
      script: "server.js",
      cwd: "./.next/standalone",
      instances: 1,
      exec_mode: "fork",
      // Страховка на VPS с ограниченной RAM: если процесс всё же уйдёт в рост,
      // pm2 перезапустит его сам, не доводя дело до swap/зависания машины.
      max_memory_restart: "700M",
      // Было: если после git pull забыли собрать проект (npm run build) и
      // просто дёрнули pm2 restart — server.js не существует, pm2 молча
      // уходит в бесконечный цикл перезапуска (десятки попыток, пока кто-то
      // не заметит). С этими двумя опциями pm2 сдаётся после 5 попыток за
      // 10 секунд и помечает процесс "errored" — ошибка видна сразу в
      // `pm2 status`, а не только в логах.
      min_uptime: "10s",
      max_restarts: 5,
      env: {
        NODE_ENV: "production",
        PORT: 3001,
        HOSTNAME: "0.0.0.0",
      },
    },
  ],
};
