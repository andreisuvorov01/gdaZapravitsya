export interface SeoPageStats {
  total: number;
  yes: number;
  low: number;
  no: number;
  unknown: number;
}

export function statsFromCounts(counts: Record<FuelStatus, number>, total: number): SeoPageStats {
  return { total, ...counts };
}

function statsText(stats: SeoPageStats, prep: string): string {
  if (stats.total === 0) {
    return `Свежих отчётов по ${prep} пока мало. Откройте карту «${SITE_NAME}» или оставьте первую отметку на ближайшей АЗС.`;
  }
  const withFuel = stats.yes + stats.low;
  return `На «${SITE_NAME}» в подборке ${stats.total} АЗС: с топливом по отчётам — ${withFuel} (есть — ${stats.yes}, мало или лимит — ${stats.low}), нет — ${stats.no}. Это народные отметки, не данные сетей — перед заездом лучше перепроверить на заправке.`;
}

